'use strict';

module.exports = function (gulp, plugins) {

    return function () {
        try {

            gulp.src('./node_modules/requirejs/require.js')
                .pipe(plugins.rename('require-2.3.5-min.js'))
                .pipe(gulp.dest('./wwwroot/libs'));
        }
        catch (e) {
            return -1;
        }
        return 0;
    };
};