'use strict';

module.exports = function (gulp, plugins) {

    return function () {
        try {

            gulp.src('./node_modules/jquery/dist/jquery.min.js')
                .pipe(plugins.rename('jquery-3.3.1.min.js'))
                .pipe(gulp.dest('./wwwroot/libs'));
        }
        catch (e) {
            return -1;
        }
        return 0;
    };
};