'use strict';

module.exports = function (gulp, plugins) {

    return function () {
        try {
            gulp.src('./node_modules/handlebars/dist/handlebars.runtime.min.js')
                .pipe(plugins.rename('handlebars.runtime-4.0.11.min.js'))
                .pipe(gulp.dest('./wwwroot/libs'));
        }
        catch (e) {
            return -1;
        }
        return 0;
    };
};